    public StaffOrders(Staff staff) {
        loggedInStaff = staff;

        initComponents();
        
        DBManager db = new DBManager();
        
        HashMap<String, Customer> customers = db.loadCustomer();
        
        customers = db.loadCustomerOrders(customers);
        
        DefaultTableModel ordersModel = (DefaultTableModel)tblOrders.getModel();
        
        for(Map.Entry<String, Customer> customerEntry : customers.entrySet())//loop through all zooKeepers
        {
            Customer actualZooKeeper = customerEntry.getValue();
            for(Map.Entry<Integer, Order> orderMapEntry : actualZooKeeper.getOrders().entrySet()) // for each order
            {
                Order actualOrder = orderMapEntry.getValue();

                ordersModel.addRow(new Object[] {
                    customerEntry.getKey(),
                    actualOrder.getOrderId(),
                    new SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(actualOrder.getOrderDate()),
                    "Â£" + decfor.format(actualOrder.getOrderTotal())
                });
            }
        }

    }

