    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        int productId = Integer.parseInt(txtID.getText());
        String name = txtName.getText();
        
        try{
            int stockLevel = Integer.parseInt(txtStockLevel.getText());
            try{
            double price = Double.parseDouble(txtPrice.getText());
            if(productToEdit.getClass().getName().equals("models.Clothing"))
            {
                    String measurement = txtExtraAttribute.getText();
                    //Clothing(String measurementIn, int productIdIn, String productNameIn, double priceIn, int stockLevelIn)
                    Clothing clothing = new Clothing(measurement,productId, name, price, stockLevel);
                
                    DBManager db = new DBManager();
                    db.modifyProduct(clothing);
                
                    lblConfirmation.setText("Successfully modified product.");
                            
            }else
            {                
                    int size = Integer.parseInt(txtExtraAttribute.getText());
                    //Footwear(int sizeIn, int productIdIn, String productNameIn, double priceIn, int stockLevelIn)
                    Footwear footwear = new Footwear(size,productId,name,price,stockLevel);
                    
                    DBManager db = new DBManager();
                    db.modifyProduct(footwear);
                    
                    lblConfirmation.setText("Successfully modified product.");
            }

                    }
                    catch(Exception ex)
                    {
                        lblConfirmation.setText("Error:Enter whole number as Stock Level");
                    }
        }
        catch(Exception ex)
        {
            lblConfirmation.setText("Error: Enter decimal number as Price");
        }

    }//GEN-LAST:event_btnSubmitActionPerformed

